"""
A simple calculator module that provides basic mathematical operations.

"""


def add(num_1, num_2):
    """
    Add two numbers
    """
    return num_1 + num_2


def subtract(num_1, num_2):
    """
    Subtract two numbers
    """
    return num_1 - num_2


def devide(num_1, num_2):
    """
    Devide two numbers
    """

    return num_1 / num_2


def multiply(num_1, num_2):
    """
    Multiply two numbers
    """
    return num_1 * num_2
